a = int(input("შემოიტანე a: ") or 1)
b = int(input("შემოიტანე b: "))
c = int(input("შემოიტანე c: "))

d = (b**2)-(4*a*c)

x1 = (-b + d**(1/2))/2*a
x2 = (-b - d**(1/2))/2*a

if d > 0:
    print("განტოლებას აქვს ორი ამონახსნი", x1, x2)
elif d == 0:
    print("განტოლებას აქვს ერთი ნამდვილი ამონახსნი", x1 or x2)
else:
    print("განტოლებას არ აქვს ამონახსნი")
